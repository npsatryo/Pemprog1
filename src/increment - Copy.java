// Perintah Operator

package Latihan.sesi1.bin;

public class increment
{
	public static void main(String[] Xx)
	{
		int a = 120;
		System.out.println("contoh postfix increment : ");
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		a = 120;
		System.out.println("contoh postfix increment : ");
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		a = 120;
		System.out.println("contoh postfix increment : ");
		System.out.println(a);
		System.out.println(a--);
		System.out.println(a);
		a = 120;
		System.out.println("contoh postfix increment : ");
		System.out.println(a);
		System.out.println(--a);
		System.out.println(a);
	}
}